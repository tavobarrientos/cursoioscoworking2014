- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    NSManagedObject *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    NSString *edad = [[object valueForKey:@"edad"] stringValue];
    NSString *nombre = [object valueForKey:@"nombre"];
    NSString *apellido = [object valueForKey:@"apellido"];
    
    cell.textLabel.text = [NSString stringWithFormat:@"%@, %@", apellido, nombre];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"Edad: %@", edad];
}

#pragma mark - Agregar Alumno Delegate
-(void)agregarAlumnoWithNombre:(NSString *)nombre Apellido:(NSString *)apellido andEdad:(NSString *)edad {
    NSManagedObjectContext *context = [self.fetchedResultsController managedObjectContext];
    NSEntityDescription *entity = [[self.fetchedResultsController fetchRequest] entity];
    NSManagedObject *newManagedObject = [NSEntityDescription insertNewObjectForEntityForName:[entity name] inManagedObjectContext:context];
    
    [newManagedObject setValue:nombre forKey:@"nombre"];
    [newManagedObject setValue:apellido forKey:@"apellido"];
    [newManagedObject setValue:[NSNumber numberWithInteger:[edad integerValue]] forKey:@"edad"];
    
    // Save the context.
    NSError *error = nil;
    if (![context save:&error]) {
        abort();
    }
}

#pragma mark - Segues
-(void) prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if([segue.identifier isEqualToString:@"AgregarAlumno"]) {
        AgregarAlumnoController *controller = [(UINavigationController *)segue.destinationViewController childViewControllers][0];
        controller.delegate = self;
    }
}

@end
